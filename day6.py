# 1. Simple Function (No parameters)

def say_hello():
    print("Hello, world!")

say_hello()


# 2. Function with Parameters

def greet(name):
    print("Hello,", name)

greet("Rudra")

# 3. Function with Return Value

def add(a, b):
    return a + b

result = add(5, 3)
print("Sum:", result)

